// Function to generate a personalized greeting message
function generateGreeting(name) {
    return "Hello, " + name + "! Welcome to our website.";
}

// Greeting three different people
let person1 = "Alice";
let person2 = "Bob";
let person3 = "Charlie";

console.log(generateGreeting(person1)); // Greet Alice
console.log(generateGreeting(person2)); // Greet Bob
console.log(generateGreeting(person3)); // Greet Charlie
